import XCTest
@testable import API2FileCore

final class AgentGuideGeneratorTests: XCTestCase {

    // MARK: - Helpers

    private func makeConfig(
        service: String = "test",
        displayName: String = "Test Service",
        resources: [ResourceConfig] = []
    ) -> AdapterConfig {
        AdapterConfig(
            service: service,
            displayName: displayName,
            version: "1.0",
            auth: AuthConfig(type: .bearer, keychainKey: "api2file.\(service).key"),
            resources: resources
        )
    }

    private func makeResource(
        name: String = "items",
        description: String? = nil,
        strategy: MappingStrategy = .onePerRecord,
        directory: String = "items",
        format: FileFormat = .json,
        idField: String? = nil,
        readOnly: Bool? = nil,
        interval: Int? = nil
    ) -> ResourceConfig {
        ResourceConfig(
            name: name,
            description: description,
            fileMapping: FileMappingConfig(
                strategy: strategy,
                directory: directory,
                format: format,
                idField: idField,
                readOnly: readOnly
            ),
            sync: interval != nil ? SyncConfig(interval: interval) : nil
        )
    }

    // MARK: - Root Guide Tests

    func testRootGuideContainsAllServices() {
        let services: [(serviceId: String, config: AdapterConfig)] = [
            ("monday", makeConfig(
                service: "monday",
                displayName: "Monday.com",
                resources: [makeResource(format: .csv)]
            )),
            ("notion", makeConfig(
                service: "notion",
                displayName: "Notion",
                resources: [makeResource(format: .markdown)]
            )),
            ("github", makeConfig(
                service: "github",
                displayName: "GitHub",
                resources: [makeResource(format: .json)]
            ))
        ]

        let guide = AgentGuideGenerator.generateRootGuide(
            services: services,
            serverPort: 24842
        )

        // Header
        XCTAssertTrue(guide.contains("# API2File — Cloud Data as Local Files"))
        XCTAssertTrue(guide.contains("<!-- Generated by API2File at"))

        // All services listed
        XCTAssertTrue(guide.contains("`monday/` — Monday.com (csv files)"))
        XCTAssertTrue(guide.contains("`notion/` — Notion (md files)"))
        XCTAssertTrue(guide.contains("`github/` — GitHub (json files)"))

        // How it works
        XCTAssertTrue(guide.contains("## How it works"))
        XCTAssertTrue(guide.contains("Files sync bidirectionally"))
        XCTAssertTrue(guide.contains("structured canonical records"))
        XCTAssertTrue(guide.contains(".api2file/file-links.json"))
        XCTAssertTrue(guide.contains(".api2file/cache/service.sqlite"))

        // Control API with correct port
        XCTAssertTrue(guide.contains("## Control API (localhost:24842)"))
        XCTAssertTrue(guide.contains("GET /api/services"))
        XCTAssertTrue(guide.contains("POST /api/services/:id/sync"))
        XCTAssertTrue(guide.contains("GET /api/services/:id/sql/tables"))
        XCTAssertTrue(guide.contains("POST /api/services/:id/sql/query"))
        XCTAssertTrue(guide.contains("GET /api/services/:id/sql/record?resource=...&recordId=..."))
        XCTAssertTrue(guide.contains("GET /api/services/:id/sql/open?resource=...&recordId=...&surface=canonical|projection"))
        XCTAssertTrue(guide.contains("query_and_open_first"))
    }

