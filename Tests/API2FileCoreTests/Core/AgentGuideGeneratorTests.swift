        XCTAssertTrue(guide.contains("### updates/ (json files)"))
        XCTAssertTrue(guide.contains("Activity updates from Monday.com"))
        XCTAssertTrue(guide.contains("**Strategy:** one-per-record"))

        // App suggestions
        XCTAssertTrue(guide.contains("Numbers, Excel, or any text editor"))
        XCTAssertTrue(guide.contains("VS Code or any text editor"))

        // Control API
        XCTAssertTrue(guide.contains("curl localhost:24842/api/services/monday/sync"))
        XCTAssertTrue(guide.contains("curl localhost:24842/api/services/monday/status"))
